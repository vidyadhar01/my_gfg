class Solution
{
    public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        // code goes here.
        Node* head= new Node(-1);
        Node* ans = head;
        
        Node* A=head1;
        Node* B=head2;
        
        while(A && B){
            if(A->data == B->data){
                ans->next = new Node(A->data);
                ans = ans->next;
                A = A->next;
                B = B->next;
            }
            else if(A->data > B->data){
                B = B->next;
            }
            else{
                A = A->next;
            }
        }
        return head->next;
    }
};



